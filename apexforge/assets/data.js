  // Sample dyno curves: RPM vs HP/TQ. For demo, derived from stock with simple shaping.
  function buildCurve(vehicle, hpGain, tqGain) {
    const points = [];
    const rpmMin = 2000;
    const rpmMax = vehicle.redlineRpm;
    const steps = 22;
    for (let i = 0; i <= steps; i++) {
      const rpm = Math.round(rpmMin + (i * (rpmMax - rpmMin)) / steps);
      const t = (rpm - rpmMin) / (rpmMax - rpmMin);
      const shape = Math.sin(Math.PI * t) * 0.92 + 0.08; // peaky
      const hp = vehicle.stockHp * (0.78 + 0.22 * shape) + hpGain * (0.35 + 0.65 * shape);
      const tq = vehicle.stockTqNm * (0.82 + 0.18 * (1 - t * 0.7)) + tqGain * (0.55 + 0.45 * (1 - t * 0.4));
      points.push({ rpm, hp: Math.round(hp), tq: Math.round(tq) });
    }
    return points;
  }

